package com.mytaxi.android_demo.model;

public class Driver {
    private String initials;
    private String fullName;
    public Driver(){
        this.initials = "sa" ;
        this.fullName = "Sara Scott";
    }

    public String getInitials() {
        return initials;
    }

    public String getFullName() {
        return fullName;
    }
}
